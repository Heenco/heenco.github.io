async function submitCall() {
  error.value = ''
  if (!form.name.trim() || !form.trade || !form.phone.trim()) {
    error.value = 'Please fill in all fields.'
    return
  }
  loading.value = true
  try {
    await $fetch('/api/vapi/call', {
      method: 'POST',
      body: { name: form.name, trade: form.trade, phone: form.phone },
    })
    success.value = true
  } catch (err: any) {
    error.value = err?.data?.message ?? err?.message ?? 'Something went wrong. Please try again.'
  } finally {
    loading.value = false
  }
}

function reset() {
  success.value = false
  form.name  = ''
  form.trade = ''
  form.phone = ''
  error.value = ''
}
